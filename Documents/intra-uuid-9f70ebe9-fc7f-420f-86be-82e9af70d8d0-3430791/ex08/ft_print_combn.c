#include <unistd.h>

void	ft_print_combn(int n)
{
	int i;
	int j;

	if (0 < n)
	{
		if (n < 10)
		{
			i = 47;
			while (++i < 57)
			{
				j = i;
				while (++j < 58)
				{
					write(1, &i, 1);
					write(1, &j, 1);
					if (i != 56)
					{
						write(1, ",", 1);
						write(1, " ", 1);
					}
				}
			}
		}
	}
}
