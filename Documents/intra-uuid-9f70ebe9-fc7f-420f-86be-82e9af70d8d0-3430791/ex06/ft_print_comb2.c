#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int n[2];

	n[0] = -1;
	while (++n[0] <= 98)
	{
		n[1] = n[0];
		while (++n[1] <= 99)
		{
			ft_putchar(n[0] / 10 + '0');
			ft_putchar(n[0] % 10 + '0');
			ft_putchar(' ');
			ft_putchar(n[1] / 10 + '0');
			ft_putchar(n[1] % 10 + '0');
			if (n[1] <= 99)
				if (n[0] != 98)
				{
					write(1, ",", 1);
					write(1, " ", 1);
				}
		}
	}
}
